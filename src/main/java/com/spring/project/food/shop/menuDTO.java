package com.spring.project.food.shop;

public class menuDTO {
	private String p_shop;
	private String menu_name;
	private String price;
	private String sale_check;
	private String recommendation;
	public String getP_shop() {
		return p_shop;
	}
	public void setP_shop(String p_shop) {
		this.p_shop = p_shop;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getSale_check() {
		return sale_check;
	}
	public void setSale_check(String sale_check) {
		this.sale_check = sale_check;
	}
	public String getRecommendation() {
		return recommendation;
	}
	public void setRecommendation(String recommendation) {
		this.recommendation = recommendation;
	}
	
	
	

}
